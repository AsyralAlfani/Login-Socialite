            <h4 style="color:#203296;font-size:22px;line-height: 1.4;font-weight: 300;">Though plants have always figured prominently in human history, our profound ability to affect deliberate, meaningful changes in plant biology reshaped the course of humankind. Domestication of grains more than 10,000 years ago marked early man’s transition from hunter-gather. Selective breeding has altered food and other crop types for many generations, facilitating growth and mobility on a massive scale.</h4>
        </p>
        <p>Plant biology and ecosystems are complex. Plant health is dramatically affected by interactions with other plants and animals, microorganisms, and the environment. Our holistic approach to engineered biology offers us the ability to modulate these factors as well as direct changes to plant biology at the genomic level with unmatched precision and specificity. Through our innovative technologies we address the impact of food demand and resource scarcity, escalating challenges of our rapidly growing population and evolving ecosystem.</p>
        <p><strong>Challenge</strong><br>
        About 40% of apples grown each year are wasted, primarily because of superficial damage and browning. As a result, preservatives are often used in an attempt to maintain freshness and color of prepared fruits, negatively affecting the taste and texture.</p>
        <p><strong>Solution</strong><br>
        By reducing the amount of polyphenol oxidase (PPO), the enzyme that causes browning, our apples won’t brown when bitten, sliced, or bruised like conventional fruits. And it means that we can distribute pre-sliced apples, thus enabling a healthy, wholesome and convenient consumer experience.</p>
        <p><strong>Challenge</strong><br>
        Drought and disease have the potential to devastate crops of all types. Flowering of vegetative crops like alfalfa reduces yield and foraging.</p>
        <p><strong>Solution</strong><br>
        Our genetic control systems allow for expression of traits on-demand, delaying flowering to prolong crop foraging or to increase biomass and feed quality. This ability to switch traits on and off can also give crops a genetic edge to battle plant disease or periods of drought.</p>
        <p><strong>Challenge</strong><br>
        Conventional agriculture is inadequate to meet the growing consumer demand for locally grown crops with consistent quality and year-round availability. Development of crops with improved productivity and consumer appeal through traditional means of breeding is costly, labor-intensive and slow.</p>
        <p><strong>Solution</strong><br>
        Our integrated and novel approach with Botticelli™ platform enables seedless propagation of high value crops and speed to market with appealing traits for both consumers and growers.</p>
        <p><strong>Challenge</strong><br>
        Effective crop production depends on both promotion of beneficial plant-microbe interactions and mitigation of the effects of harmful microbes that can lead to disease or damage.</p>
        <p><strong>Solution</strong><br>
        intrexon is advancing precision engineering of plants and microbes in the context of their interactions to promote improved production of crops.</p>
