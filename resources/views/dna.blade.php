        <p>
            <h4 style="color:#203296;font-size:22px;line-height: 1.4;font-weight: 300;">For generations, humankind has sought remedies for the ailments that threaten lives and wellbeing. Breakthroughs in medical science have given us a deeper understanding of human biology, leading to life-saving therapies, medications, and vaccines.</h4>
        </p>
        <p>Nearly two decades beyond the mapping of the human genome, we are experiencing a “Post-Genome Project” renaissance. Advances in engineered biology drive interventions at the genetic level, with the potential to address conditions once thought to be beyond human understanding.</p>
<p>intrexon is innovating new approaches to gene and cell therapies with targeted solutions to address underlying causes of disease and advance complementary technologies. With precision biology we are working in ways previously unimagined to make treatments safer, more effective and less expensive to provide for the unmet needs of patients.</p>
<p><strong>Challenge</strong><br>
The challenge of maintaining human health is enormous. Breakthrough discoveries over generations are improving outcomes and advancing treatment options each day. Yet treatments for complex diseases—including those that cannot be traced to a single genetic marker—remain out of reach or come with costs too great for many.</p>
<p><strong>Solutions</strong><br>
We are dedicated to advancing the next generation of controlled, targeted, multigenic gene and cellular therapies using precision technologies to address chronic, infectious and rare diseases including cancer, cardiac disease, autoimmune and metabolic disorders. Our platform technologies also have applications for detection of biothreats and disease states, as well as preventative measures including next generation molecular vaccines.</p>
<ul>
<li>Multigenic Therapy<br>
Many complex conditions like heart disease are the result of multiple genetic factors, rather than a single gene defect. We are pioneering a shift from monogenic to multi-gene therapies, targeting multiple genes with a single integrated therapy.</li>
<li>Point-of-Care CAR-T Cell Therapy<br>
Unlike other CAR-T cell therapies, our non-viral approach enables engineering of patient cells in the clinical point-of-care setting for a more rapid administration of therapy, rather than through time-consuming offsite processes for cell expansion. This approach should dramatically reduce patient treatment wait time while lowering costs.</li>
<li>Immunotherapy<br>
Delivering the correct dosage of a potent immune modulator to treat brain cancer is highly challenging, and carries a significant risk of adverse effects from incorrect dosing. With a pill, we can regulate the expression of administered therapies via a controlled gene switch, enabling physicians to turn an immunotherapy on or off and modulate dosage levels according to patient response.</li>
<li>Cell Therapy<br>
Numerous pathologies are the result of protein or molecular deficiencies. Replacement therapies provide a means to kickstart cells into producing the deficient proteins or molecules. We are advancing treatments for devastating rare skin diseases requiring engineered cells to alleviate the pathology and restore function, improving condition of the skin and providing relief for patients.</li>
<li>Molecular Vaccines<br>
New approaches to production and administration of vaccines addressing biothreats from emerging epidemics or bad actors are expanding the menu of medical countermeasures available to the biodefense and public health communities. Next generation DNA-based vaccines offer a wider range of immune response types against chronic and infectious diseases, as well as, production, supply chain, and administration benefits without the risk for infection.</li>
</ul>
