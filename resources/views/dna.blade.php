        <p>
            <h4 style="color:#203296;font-size:22px;line-height: 1.4;font-weight: 300;">Animal and human health are inextricably linked. Whether livestock and fish that contribute to our food system, or insects in the wild, the health of non-human animals inevitably affects our own.</h4>
        </p>
        <p>Use (and overuse) of antibiotics in cattle. Transmission of deadly diseases by airborne vectors like mosquitoes. Depletion of oceans due to over-fishing. Though the planetary impact of population growth is undeniable, so too is our ability to mitigate those impacts.</p>
<p>Precision bioengineering is the answer.</p>
<p><strong>Challenge</strong><br>
Protein production must increase by 70% to meet the demands of global populations in 2050. Scarcity of arable land, access to water, transportation needs, antibiotic use and other factors increasingly will produce negative environmental impacts.</p>
<p><strong>Solution</strong><br>
Our elite genetics programs advance and propagate desirable traits via selective breeding. In addition to making our elite germplasm available to breeders, we are working to enhance livestock production via stacking of engineered traits on top of elite genetics to benefit overall health, disease resistance, breeding efficiencies, feed consumption rates, and environmental impacts. The result is high-quality livestock with characteristics that improve production and increase appeal for consumers.</p>
<p><strong>Challenge</strong><br>
Populations of ocean fish are declining and the negative impacts of open ocean traditional fish farming are growing. Reliance on antibiotics and animal exposure to marine pathogens make marine farming even more challenging.</p>
<p><strong>Solution</strong><br>
intrexon next-generation inland fish production<br>
Our engineered fish stocks grow faster than their wild counterparts and require less feed to reach maturity, while eliminating the need for antibiotics and removing the impact on ocean habitats.</p>
<p><strong>Challenge </strong><br>
Infectious disease treatments and crop damage from insect pests cost billions of dollars each year. Pesticides become less effective over time, often requiring larger quantities and higher potencies to do the job. Risk to water sources, land, healthy air, and beneficial insects and animals in our ecosystems increases as a result.</p>
<p><strong>Solution</strong><br>
Self-limiting insects<br>
We’ve engineered self-limiting insects to pass along a gene that prevents the survival of offspring in order to control pest populations without off-target effects. Innovative, traceable biomarkers enable controlled release of engineered insects to reduce wild pest populations with minimal environmental impact.</p>
<p><strong>Challenge </strong><br>
Medical researchers rely on mice and other small-animal disease models to predict outcomes of treatments and medications. Yet this approach cannot fully reproduce a human disease condition, impeding the development of therapeutics and diagnostic tools.</p>
<p><strong>Solution</strong><br>
Innovative animal models with engineered traits<br>
Animal research models can now be engineered with greater genetic precision to advance disease-specific phenotypes, improving the predictive power of clinical research and therapeutic development.</p>
<p><strong>Additional Solutions </strong></p>
<ul>
<li>Engineering of animal-microbe interactions to promote health and well-being.</li>
<li>Mitigation of invasive species burdening our ecosystems.</li>
<li>Biological engineering of sustainable animal feeds that divert and reduce food waste.</li>
<li>Improved treatments for companion animals by translating cancer, diabetes and other therapies to cats, dogs and horses.</li>
</ul>
