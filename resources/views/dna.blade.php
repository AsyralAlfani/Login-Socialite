            <h4 style="color:#203296;font-size:22px;line-height: 1.4;font-weight: 300;">Microbes play an invisible but critical role in nearly every aspect of our interconnected ecosystems, from protective bacteria that contribute to human health, to destructive microorganisms that wreak havoc on the environment.</h4>
        </p>
        <p>intrexon develops new technologies and novel applications for microbes that address many of the planet’s most pressing concerns: making better use of finite natural resources, improving medical treatments, and providing alternative fuel production approaches to reinvent our energy systems.</p>
        <p><strong>Challenge</strong><br>
        Traditional methods of fuel and chemical production are costly, wasteful and drive significant environmental impact.</p>
        <p><strong>Solution</strong><br>
        intrexon’s methane bioconversion platform (MBP) turns natural gas into more valuable and usable energy and chemical products via single-step fermentation. This environmentally friendly production technology has the potential to transform production of drop-in fuels and rubber and plastic materials.</p>
        <p><strong>Challenge</strong><br>
        Targeted biological therapies show enormous promise for disease treatment, with many available to patients today. However, treatment costs remain high while safety and efficacy remain suboptimal.</p>
        <p><strong>Solution</strong><br>
        We are pioneering a new class of microbe-based biopharmaceuticals that enable expression and local delivery of disease-modifying therapeutics, including therapeutic proteins targeting multiple disease pathways. Our oral or topical administration capabilities offer the promise of safer, more economical, and more efficacious treatments than injectable biologics.</p>
